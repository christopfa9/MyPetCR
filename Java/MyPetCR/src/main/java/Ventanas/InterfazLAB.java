    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        int proceso = cmbProceso.getSelectedIndex();
        
        filasAfectadas = 0;
        
        if (proceso == 0) { //Consultar todos los autores
            tabla = AutoresAccess.getAutores();
            if (!tabla.isEmpty()) {
                Auxiliar.addTabla(tabla, jTableAutores);
            }
        } else if (proceso == 1){ //Consultar un autor
            idAutor = txtIDAutor.getText();
            if (idAutor.equals("")) {
                JOptionPane.showMessageDialog(null, "Debe ingresar un ID de Autor", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                tabla = AutoresAccess.getAutor(idAutor);
                if (!tabla.isEmpty()) {
                    Auxiliar.addTabla(tabla, jTableAutores);
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo encontrar el autor", "Error", JOptionPane.ERROR_MESSAGE);
                }
                 clearEspacios();
            }
            
        } else if (proceso == 2) { //Insertar un autor
            if (getValores()){ //Obtener los valores de los textFields, etc;
                filasAfectadas = AutoresAccess.insertAutor(idAutor, nombreAutor, 
                    idPais, vivo, fechaNacimiento, fechaPublicacion, 
                    publicaciones, calificacion);
                clearEspacios();
            }
            JOptionPane.showMessageDialog(null, filasAfectadas + " filas fueron afectadas", "Información", JOptionPane.INFORMATION_MESSAGE);             
        } else if (proceso == 3) { //Actualizar un autor
            if (getValores()){ //Obtener los valores de los textFields, etc;
                filasAfectadas = AutoresAccess.updateAutor(idAutor, nombreAutor, 
                        idPais, vivo, fechaNacimiento, fechaPublicacion, 
                        publicaciones, calificacion);
                clearEspacios();
            }
            JOptionPane.showMessageDialog(null, filasAfectadas + " filas fueron afectadas", "Información", JOptionPane.INFORMATION_MESSAGE);
        } else if (proceso == 4) { //Eliminar un autor
            idAutor = txtIDAutor.getText();
            if (idAutor.equals("")) {
                JOptionPane.showMessageDialog(null, "Debe ingresar un ID de Autor", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                filasAfectadas = AutoresAccess.deleteAutor(idAutor);
                clearEspacios();
            }
            JOptionPane.showMessageDialog(null, filasAfectadas + " filas fueron afectadas", "Información", JOptionPane.INFORMATION_MESSAGE);
        }
        
        
        
        
    }//GEN-LAST:event_btnAceptarActionPerformed

