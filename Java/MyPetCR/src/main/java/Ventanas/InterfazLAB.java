    private boolean getValores () {
        idAutor = txtIDAutor.getText();
        if (idAutor.equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un ID de Autor", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        nombreAutor = txtNombre.getText();
        if (nombreAutor.equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un Nombre de Autor", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        idPais = txtID_Pais.getText();
        if (idPais.equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un ID de Pais valido", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        String tmp;
        tmp = ((JTextField) dateNacimiento.getDateEditor().getUiComponent()).getText();
        if (tmp.equals("")){
            JOptionPane.showMessageDialog(null, "Debe ingresar fechas validas", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            fechaNacimiento = Date.valueOf(tmp);
        }
        
        
        tmp = ((JTextField) datePublicacion.getDateEditor().getUiComponent()).getText();
        if (tmp.equals("")){
            JOptionPane.showMessageDialog(null, "Debe ingresar fechas validas", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            fechaPublicacion = Date.valueOf(tmp);
        }
        if (fechaNacimiento != null && fechaPublicacion != null){
            if (!validarFechas()) {
                return false;
            }
        }
        // Obtener fecha actual
        if (radioVivo.isSelected())
            vivo = "Y";
        else 
            vivo = "N";

        publicaciones = Integer.parseInt(spinNumPublis.getValue().toString());
        calificacion = new BigDecimal(spinCalificacion.getValue().toString());

        return true;
    }
    
    private boolean validarFechas(){
        Calendar cal = Calendar.getInstance();
        Date fechaActual = new Date(cal.getTimeInMillis());
        
        if (fechaNacimiento.after(fechaActual) || fechaPublicacion.after(fechaActual)) {
            JOptionPane.showMessageDialog(null, "Las fechas no pueden ser mayores que la fecha actual", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (fechaNacimiento.after(fechaPublicacion)) {
            JOptionPane.showMessageDialog(null, "La fecha de nacimiento debe ser anterior a la fecha de publicación", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

